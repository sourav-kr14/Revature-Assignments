Create database ECOMMERCEDB;

USE ECOMMERCEDB;

CREATE TABLE CUSTOMERS(
CUSTOMERID INT,
CUSTOMERNAME VARCHAR(50),
CITY VARCHAR(40),
EMAIL VARCHAR(100) 
);

SELECT * FROM CUSTOMERS;
DESC CUSTOMERS;

CREATE TABLE PRODUCTS(
PRODUCTID INT,
PRODUCTNAME VARCHAR(50),
CATEGORY VARCHAR(40),
PRICE INT
);


SELECT * FROM PRODUCTS;
DESC PRODUCTS;


CREATE TABLE ORDERS(
ORDERID INT,
CUSTOMERID INT,
ORDERDATE DATE,
TOTALAMOUNT INT
);

SELECT * FROM ORDERS;
DESC ORDERS;

CREATE TABLE ORDERITEMS(
ORDERITEMID INT,
ORDERID INT,
PRODUCTID INT,
QUANTITY INT
);


SELECT * FROM ORDERITEMS;
DESC ORDERITEMS;

